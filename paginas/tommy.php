<p>
    O jogo de plataforma Tommy's Adventure é inspirado em jogos estilo Endless Runner que são jogos de corrida
    intermináveis, como exemplo o Dino T-Rex, o famoso dinossauro do Google que aparece quando a internet cai. No
    Tommy's Adventure, o jogador iniciará a partida já em movimento e deverá seguir a diante tomando cuidado com os
    obstáculos e trajetos incompletos que implicarão direto no resultado do jogo.
    Embarque em Tommy's Adventure, onde cada passo pode ser seu último e apenas os mais ágeis sobreviverão!
